class Wrapper{
	
	edgeId;
	sourceId;
	sourceDegree;
	sourceX;
	sourceY;
	targetId;
	targetDegree;
	targetX;
	targetY;
	
	constructor(edgeId, sourceId, sourceDegree, sourceX, sourceY. targetId, targetDegree, targetX, targetY){
		this.edgeId = edgeId;
		this.sourceId = sourceId;
		this.sourceDegree = sourceDegree;
		this.sourceX = sourceX;
		this.sourceY = sourceY;
		this.targetId = targetId;
		this.targetDegree = targetDegree;
		this.targetX = targetX;
		this.targetY = targetY;
	}
}